// index.js is used to setup and configure your bot

// Import required packages
const path = require('path');

// Note: Ensure you have a .env file and include LuisAppId, LuisAPIKey and LuisAPIHostName.
const ENV_FILE = path.join(__dirname, '.env');
require('dotenv').config({ path: ENV_FILE });

const restify = require('restify');

// Import required bot services.
const { BotFrameworkAdapter, ConversationState, InputHints, MemoryStorage, UserState, ShowTypingMiddleware } = require('botbuilder');

const { incidentRecognizer } = require('./dialogs/incidentRecognizer');

// This bot's main dialog.
const { DialogAndWelcomeBot } = require('./bots/dialogAndWelcomeBot');
const { MainDialog } = require('./dialogs/mainDialog');

//Dialog for creating a new incident
const { AccessDialog } = require('./dialogs/AccessDialog');
const ACCESS_DIALOG = 'accessDialog';

//Dialog for creating a new incident
const { SolutionDialog } = require('./dialogs/SolutionDialog');
const SOLUTION_DIALOG = 'solutionDialog';


//Dialog for creating a new incident
const { CreateChatIncidentDialog } = require('./dialogs/CreateChatIncidentDialog');
const CREATE_CHAT_INCIDENT_DIALOG = 'createChatIncidentDialog';

//Dialog for updating an incident
const { ReassignIncidentDialog } = require('./dialogs/ReassignIncidentDialog');
const REASSIGN_INCIDENT_DIALOG = 'reassignIncidentDialog';

//Dialog for updating an incident
const { CloseIncidentDialog } = require('./dialogs/CloseIncidentDialog');
const CLOSE_INCIDENT_DIALOG = 'closeIncidentDialog';

// Create adapter.
const adapter = new BotFrameworkAdapter({
    appId: process.env.MicrosoftAppId,
    appPassword: process.env.MicrosoftAppPassword
});

// Catch-all for errors.
const onTurnErrorHandler = async (context, error) => {
    // This check writes out errors to console log
    console.error(`\n [onTurnError] unhandled error: ${ error }`);

    // Send a trace activity, which will be displayed in Bot Framework Emulator
    await context.sendTraceActivity(
        'OnTurnError Trace',
        `${ error }`,
        'https://www.botframework.com/schemas/error',
        'TurnError'
    );

    // Send a message to the user
    let onTurnErrorMessage = 'The bot encountered an error or bug.';
    await context.sendActivity(onTurnErrorMessage, onTurnErrorMessage, InputHints.ExpectingInput);
    onTurnErrorMessage = 'To continue to run this bot, please fix the bot source code.';
    await context.sendActivity(onTurnErrorMessage, onTurnErrorMessage, InputHints.ExpectingInput);
    // Clear out state
    await conversationState.delete(context);
};

// Set the onTurnError for the singleton BotFrameworkAdapter.
adapter.onTurnError = onTurnErrorHandler;

// Define a state store for your bot. See https://aka.ms/about-bot-state to learn more about using MemoryStorage.
// A bot requires a state store to persist the dialog and user state between messages.

// For local development, in-memory storage is used.
// CAUTION: The Memory Storage used here is for local bot debugging only. When the bot
// is restarted, anything stored in memory will be gone.
const memoryStorage = new MemoryStorage();
const conversationState = new ConversationState(memoryStorage);
const userState = new UserState(memoryStorage);

// If configured, pass in the incidentRecognizer.  (Defining it externally allows it to be mocked for tests)
const { LuisAppId, LuisAPIKey, LuisAPIHostName } = process.env;
const luisConfig = { applicationId: LuisAppId, endpointKey: LuisAPIKey, endpoint: `https://${ LuisAPIHostName }` };

const luisRecognizer = new incidentRecognizer(luisConfig);

// Create the main dialog.

const accessDialog = new AccessDialog(ACCESS_DIALOG);
const solutionDialog = new SolutionDialog(SOLUTION_DIALOG);
const createChatIncidentDialog = new CreateChatIncidentDialog(CREATE_CHAT_INCIDENT_DIALOG);
const reassignIncidentDialog = new ReassignIncidentDialog(REASSIGN_INCIDENT_DIALOG);
const closeIncidentDialog = new CloseIncidentDialog(CLOSE_INCIDENT_DIALOG);
const dialog = new MainDialog(luisRecognizer, accessDialog, solutionDialog, createChatIncidentDialog, reassignIncidentDialog, closeIncidentDialog);
const bot = new DialogAndWelcomeBot(process.env.ExpireAfterSeconds, conversationState, userState, dialog);

// Create HTTP server
const server = restify.createServer();
server.listen(process.env.port || process.env.PORT || 3978, function() {
    console.log(`\n${ server.name } listening to ${ server.url }`);
    console.log('\nGet Bot Framework Emulator: https://aka.ms/botframework-emulator');
    console.log('\nTo talk to your bot, open the emulator select "Open Bot"');
});

// Listen for incoming activities and route them to your bot main dialog.
server.post('/api/messages', (req, res) => {
    // Route received a request to adapter for processing
    adapter.processActivity(req, res, async (turnContext) => {
        // route to bot activity handler.
        await bot.run(turnContext);
    });
});

// Listen for Upgrade requests for Streaming.
server.on('upgrade', (req, socket, head) => {
    // Create an adapter scoped to this WebSocket connection to allow storing session data.
    const streamingAdapter = new BotFrameworkAdapter({
        appId: process.env.MicrosoftAppId,
        appPassword: process.env.MicrosoftAppPassword
    });
    // Set onTurnError for the BotFrameworkAdapter created for each connection.
    streamingAdapter.onTurnError = onTurnErrorHandler;

    streamingAdapter.useWebSocket(req, socket, head, async (context) => {
        // After connecting via WebSocket, run this logic for every request sent over
        // the WebSocket connection.
        await bot.run(context);
    });
});
